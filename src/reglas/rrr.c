#include "pushswap.h"

void	rrr(t_list **a, t_list **b)
{
	if (*b == NULL || (*b)->next == NULL)
		return ;
	if (*a == NULL || (*a)->next == NULL)
		return ;
	rra(a, 1);
	rrb(b, 1);
}
