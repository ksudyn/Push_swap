#include "../includes/pushswap.h"

void rb(t_list *dig)
{
	int	i;

	i = 0;
	while (i + 1 < dig->sib)
	{
		swap(&dig->sb[i], &dig->sb[i + 1]);
		i++;
	}
	write(1, "rb\n", 3);
}
